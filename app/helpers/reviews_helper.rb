module ReviewsHelper
  def review_path(*prefix_or_suffix)
    prefix_or_suffix = prefix_or_suffix.length == 1 ? prefix_or_suffix[0] : nil

    if @entity.class == Candidate then path = 'candidate_review'
    elsif @entity.class == Company then path = 'company_review'
    end
    path+'_'+'path'

  end

  def review_path_params(review)
    id = review.id
    @entity.instance_of?(Candidate) ? {candidate_id: params[:candidate_id],id: id} : {company_id: params[:company_id],id: id}
  end
end
